// 1️⃣ Basic Strings
let firstName = "Jay";
let lastName = "Joshi";
console.log("Full Name:", firstName + " " + lastName);

// 2️⃣ String Length
console.log("Length of firstName:", firstName.length);

// 3️⃣ Changing Case
console.log("Uppercase:", firstName.toUpperCase());
console.log("Lowercase:", lastName.toLowerCase());

// 4️⃣ Checking Substring
console.log("Includes 'Jo'?", lastName.includes("Jo"));

// 5️⃣ Template Literals
console.log(`Hello ${firstName} ${lastName}, welcome!`);

// 6️⃣ Reassigning Strings
firstName = "Jayesh";
console.log("Updated Name:", firstName);
