// 1️⃣ Arithmetic Operators
let a = 10;
let b = 5;

console.log("Addition:", a + b);         // 15
console.log("Subtraction:", a - b);      // 5
console.log("Multiplication:", a * b);   // 50
console.log("Division:", a / b);         // 2
console.log("Modulus:", a % b);          // 0

// 2️⃣ Comparison Operators
console.log("Is a > b?", a > b);         // true
console.log("Is a < b?", a < b);         // false
console.log("Is a == b?", a == b);       // false
console.log("Is a != b?", a != b);       // true

// 3️⃣ Logical Operators
let isAdult = true;
let hasPermission = false;

console.log("AND (&&):", isAdult && hasPermission);  // false
console.log("OR (||):", isAdult || hasPermission);   // true
console.log("NOT (!):", !isAdult);                  // false

// 4️⃣ Using operators in expressions
let total = a + b * 2; 
console.log("Total (a + b*2):", total);
