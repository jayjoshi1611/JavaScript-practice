// Day 1: Basic Variables Practice
// ================================

// 1️⃣ Number Variable
let age = 18;
age = 25; // value update
console.log("Age:", age);

// 2️⃣ String Variables
let firstName = "Jay";
let lastName = "Joshi";
console.log("Full Name:", firstName + " " + lastName);

// 3️⃣ Boolean Variable
let isStudent = true;
console.log("Is Student?", isStudent);

// 4️⃣ Multiple Variables & Math
let x = 10;
let y = 5;
let sum = x + y;
let difference = x - y;
let product = x * y;
let division = x / y;
console.log("Sum:", sum);
console.log("Difference:", difference);
console.log("Product:", product);
console.log("Division:", division);

// 5️⃣ Using Variables in Strings
console.log(firstName + " is " + age + " years old.");

// 6️⃣ Reassigning Variables
age = 30;
console.log("Updated Age:", age);
